#ექმენით ფუნქცია რომელსაც გადაეცემა 2 არგუმენტი width და height, ეს არგუმენტები არის ოთკუთხედის გვერდები, თუ ეს ოთხკუთხედი არის კვადრატი მაშინ დააბრუნე მისი ფართობი, ხოლო თუ არის მართკუთხედი მაშინ დააბრუნე პერიმეტრი
def ოთხკუთხედი(width, height):
    if width == height:
        return width * height  
    else:
        return 2 * (width + height)  






